# ScenarioRunner
# ---------------------------------------------------------------------------


class ScenarioRunner(InputOrchestrator):
    """Minimum viable orchestrator for Milestone 1.

    Executes a fully-specified scenario synchronously and returns the complete
    state history as a list. Designed for scripted scenarios, the CLI demo,
    and unit tests. Implements the full InputOrchestrator interface so it
    can be replaced by the web orchestrator in Milestone 2 without changing
    any caller code.

    scheduled_inputs is a list of (step_index, ControlInput) tuples. Step 0
    is the initial state; an input at step_index=1 fires before the first
    advance (i.e. at the transition from step 0 to step 1).

    Example::

        runner = ScenarioRunner(
            initial_state=state,
            scheduled_inputs=[(1, tariff_shock)],
            modules=[],
            n_steps=10,
        )
        history = runner.run()
        # history[0] is initial_state
        # history[1] is state after step 1 with tariff_shock applied
        # history[10] is final state
    """

    def __init__(
        self,
        initial_state: SimulationState,
        scheduled_inputs: list[tuple[int, ControlInput]],
        modules: list[SimulationModule],
        n_steps: int,
        audit_log: AuditLog | None = None,
        contingent_inputs: list[ContingentInput] | None = None,
        session_id: str | None = None,
        timestep_delta: timedelta | None = None,
    ) -> None:
        """Initialise the scenario runner.

        Args:
            initial_state: Starting SimulationState (step 0).
            scheduled_inputs: (step_index, ControlInput) pairs. Multiple
                inputs at the same step index fire in list order.
            modules: SimulationModules to run each tick.
            n_steps: Total number of steps to advance. History length
                will be n_steps + 1 (including initial state).
            audit_log: AuditLog to record injected inputs. If None, an
                empty in-memory AuditLog is created.
            contingent_inputs: ContingentInputs to evaluate each step.
                None means no contingent triggers.
            session_id: Session identifier for audit records. If None,
                a UUID is generated.
            timestep_delta: Time advance per step as a fixed timedelta. Pass
                None (the default) to use calendar-year arithmetic, which
                advances by exactly one year on the calendar regardless of
                whether leap years fall in the path. Pass an explicit timedelta
                for sub-annual or non-standard timestep intervals.
        """
        from app.simulation.orchestration.audit import AuditLog as _AuditLog

        self._initial_state = initial_state
        self._scheduled_inputs = scheduled_inputs
        self._modules = modules
        self._n_steps = n_steps
        self._audit_log = audit_log if audit_log is not None else _AuditLog()
        self._contingent_inputs: list[ContingentInput] = (
            contingent_inputs if contingent_inputs is not None else []
        )
        self._session_id = session_id if session_id is not None else str(uuid.uuid4())
        # None means "use calendar-year arithmetic"; see _advance_timestep().
        self._timestep_delta: timedelta | None = timestep_delta
        # contingent_id -> remaining cooldown periods (0 means ready to fire)
        self._cooldown_remaining: dict[str, int] = {}

    @property
    def audit_log(self) -> AuditLog:
        """The audit log accumulating records for this runner's session."""
        return self._audit_log

    @property
    def session_id(self) -> str:
        """Session identifier used in all audit records from this runner."""
        return self._session_id

    def run(self) -> list[SimulationState]:
        """Execute the full scenario and return the complete state history.

        Advances n_steps times from the initial state. At each step, fires
        scheduled inputs, evaluates contingents, runs modules, and propagates.

        Returns:
            List of SimulationStates of length n_steps + 1. Index 0 is the
            initial state. Index i is the state after i steps have been taken.
        """
        history: list[SimulationState] = [self._initial_state]
        current_state = self._initial_state

        for step_index in range(1, self._n_steps + 1):
            step_inputs = [
                inp
                for (idx, inp) in self._scheduled_inputs
                if idx == step_index
            ]
            current_state = self.advance_timestep(
                current_state=current_state,
                modules=self._modules,
                scheduled_inputs=step_inputs,
            )
            history.append(current_state)
            self._decrement_cooldowns()

        return history

    def inject(
        self,
        control_input: ControlInput,
        session_id: str,
    ) -> list[Event]:
        """Translate one ControlInput to Events and record it in the audit log.

        Calls control_input.to_events() with the current simulation timestep,
        then appends a ControlInputAuditRecord capturing the full provenance.

        Args:
            control_input: The exogenous input to process.
            session_id: Session identifier for the audit record.

        Returns:
            Events generated by the input's to_events() method.
        """
        from datetime import datetime as _datetime

        from app.simulation.orchestration.audit import ControlInputAuditRecord

        now = _datetime.utcnow()
        events = control_input.to_events(control_input.effective_date or now)
        record = ControlInputAuditRecord(
            record_id=str(uuid.uuid4()),
            scenario_id=self._initial_state.scenario_config.scenario_id,
            session_id=session_id,
            timestep=control_input.effective_date or now,
            input_type=type(control_input).__name__,
            source=control_input.source,
            actor_id=control_input.actor_id,
            actor_role=control_input.actor_role,
            justification=control_input.justification,
            raw_input=_serialise_input(control_input),
            translated_events=[e.event_id for e in events],
            timestamp=now,
        )
        self._audit_log.append(record)
        return events

    def advance_timestep(
        self,
        current_state: SimulationState,
        modules: list[SimulationModule],
        scheduled_inputs: list[ControlInput],
    ) -> SimulationState:
        """Execute one full simulation tick.

        Order of operations:
        1. Check contingent triggers against current state.
        2. Inject all scheduled inputs (exogenous Events).
        3. Inject all newly-fired contingent inputs.
        4. Run all modules for every entity (endogenous Events).
        5. Merge exogenous and endogenous Events.
        6. Propagate through the relationship graph.
        7. Advance the timestep on the resulting state.

        Args:
            current_state: State[T]. Treated as read-only.
            modules: Simulation modules to run this tick.
            scheduled_inputs: Exogenous inputs to inject this tick.

        Returns:
            State[T+1] with all deltas applied and timestep advanced.
        """
        from app.simulation.engine.propagation import propagate

        all_events: list[Event] = []

        # Exogenous: inject scheduled inputs
        for control_input in scheduled_inputs:
            all_events.extend(self.inject(control_input, self._session_id))

        # Exogenous: inject contingent inputs that just triggered
        for fired_input in self.check_contingents(current_state):
            all_events.extend(self.inject(fired_input, self._session_id))

        # Endogenous: run modules for each entity
        for entity in current_state.entities.values():
            for module in modules:
                endogenous_events = module.compute(
                    entity, current_state, current_state.timestep
                )
                all_events.extend(endogenous_events)

        # Propagate all events and advance the timestep
        next_state = propagate(current_state, all_events)
        return dataclasses.replace(
            next_state,
            timestep=_advance_timestep(current_state.timestep, self._timestep_delta),
        )

    def check_contingents(self, state: SimulationState) -> list[ControlInput]:
        """Evaluate all registered ContingentInputs against current state.

        A contingent fires when its condition is met AND its cooldown has
        expired (cooldown_remaining == 0). When a contingent fires, its
        cooldown is set to cooldown_periods. The fired ControlInput has its
        source overridden to CONTINGENT_TRIGGER.

        Args:
            state: Current simulation state to evaluate conditions against.

        Returns:
            List of ControlInputs ready to fire. May be empty.
        """
        from app.simulation.orchestration.inputs import InputSource

        fired: list[ControlInput] = []
        for contingent in self._contingent_inputs:
            remaining = self._cooldown_remaining.get(contingent.contingent_id, 0)
            if remaining > 0:
                continue
            if contingent.condition.is_met(state):
                triggered_input = dataclasses.replace(
                    contingent.input,
                    source=InputSource.CONTINGENT_TRIGGER,
                )
                fired.append(triggered_input)
                self._cooldown_remaining[contingent.contingent_id] = (
                    contingent.cooldown_periods
                )
        return fired

    def _decrement_cooldowns(self) -> None:
        """Decrement all active cooldown counters by one period.

        Called at the end of each step in run(). A contingent whose counter
        reaches zero becomes eligible to fire again on the next step.
        """
        for key in list(self._cooldown_remaining.keys()):
            if self._cooldown_remaining[key] > 0:
                self._cooldown_remaining[key] -= 1


