"""
ScenarioRunner — minimum viable orchestrator for Milestone 1 (ADR-002).

ScenarioRunner is the concrete InputOrchestrator for Milestone 1. It accepts
a fully-specified scenario as Python objects (initial state, scheduled inputs,
modules) and executes it synchronously, returning the complete state history.

The full async InputOrchestrator (serving the web API) is a Milestone 2
deliverable. ScenarioRunner's interface is identical — callers that use the
InputOrchestrator ABC will work unchanged when the web orchestrator replaces
this runner.
"""

from __future__ import annotations

import dataclasses
import uuid
from abc import ABC, abstractmethod
from datetime import datetime, timedelta  # noqa: TCH003 — used at runtime in _advance_timestep
from typing import TYPE_CHECKING, Any

if TYPE_CHECKING:

    from app.simulation.engine.models import Event, SimulationModule, SimulationState
    from app.simulation.orchestration.audit import AuditLog
    from app.simulation.orchestration.inputs import (
        ContingentInput,
        ControlInput,
    )


# ---------------------------------------------------------------------------
# InputOrchestrator ABC
# ---------------------------------------------------------------------------


class InputOrchestrator(ABC):
    """Abstract base class for all input orchestrators.

    The orchestrator is the sole entry point for exogenous control inputs.
    All input channels (UI, API, CLI, contingent triggers, scenario scripts)
    converge here. Every input is validated, translated to Events, and recorded
    in the audit log before reaching the propagation engine.

    Concrete implementations:
    - ScenarioRunner: synchronous, in-process (Milestone 1)
    - WebOrchestrator: async, database-backed (Milestone 2)
    """

    @abstractmethod
    def inject(
        self,
        control_input: ControlInput,
        session_id: str,
    ) -> list[Event]:
        """Validate, translate, audit-log, and return Events for one input.

        Args:
            control_input: The exogenous input to process.
            session_id: Session context for the audit record.

        Returns:
            List of Events generated by the input's to_events() method.
        """

    @abstractmethod
    def advance_timestep(
        self,
        current_state: SimulationState,
        modules: list[SimulationModule],
        scheduled_inputs: list[ControlInput],
    ) -> SimulationState:
        """Execute one full simulation tick.

        Collects scheduled inputs (injecting each via inject()), runs all
        modules for each entity, merges exogenous and endogenous events,
        propagates through the relationship graph, and returns State[T+1].

        Args:
            current_state: State[T]. Treated as read-only.
            modules: Simulation modules to run this tick.
            scheduled_inputs: Exogenous inputs to inject this tick.

        Returns:
            State[T+1] with all deltas applied and timestep advanced.
        """

    @abstractmethod
    def check_contingents(self, state: SimulationState) -> list[ControlInput]:
        """Evaluate registered ContingentInputs against current state.

        Returns the ControlInputs whose conditions are met and whose
        cooldown has expired.

        Args:
            state: Current simulation state to evaluate conditions against.

        Returns:
            List of ControlInputs ready to fire.
        """


